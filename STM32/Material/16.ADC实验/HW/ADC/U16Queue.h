#ifndef _U16_QUEUE_H_
#define _U16_QUEUE_H_

/*********************************************************************************************************
*                                              包含头文件
*********************************************************************************************************/
#include "DataType.h"

/*********************************************************************************************************
*                                              宏定义
*********************************************************************************************************/

/*********************************************************************************************************
*                                              枚举结构体定义
*********************************************************************************************************/
//定义数据类型，即队列中元素的数据类型
typedef u8  DATA_TYPE;

//定义循环队列结构体
typedef struct
{
  i16 front;          //头指针，队非空时指向队头元素
  i16 rear;           //尾指针，队非空时指向队尾元素的下一个位置
  i16 bufLen;         //队列的总容量
  i16 elemNum;        //当前队列中的元素的数量
  u16 *pBuffer;
}StructU16CirQue;

/*********************************************************************************************************
*                                              API函数声明
*********************************************************************************************************/
void  InitU16Queue(StructU16CirQue* pQue, u16* pBuf, i16 len);  //初始化队列
void  ClearU16Queue(StructU16CirQue* pQue);                           //清队列
u8    U16QueueEmpty(StructU16CirQue* pQue);                           //判断队列是否为空，1-空，0-非空
i16   U16QueueLength(StructU16CirQue* pQue);                          //返回队列中元素个数，即为队列的长度
i16   EnU16Queue(StructU16CirQue* pQue, u16* pInput, i16 len);  //入队len个元素
i16   DeU16Queue(StructU16CirQue* pQue, u16* pOutput, i16 len); //出队len个元素

#endif

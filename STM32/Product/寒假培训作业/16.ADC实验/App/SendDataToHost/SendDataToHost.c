static  void  SendPackToHost(StructPackType* pPackSent);  //发送包到Host

/*********************************************************************************************************
*                                              内部函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称: SendPackToHost
* 函数功能: 发送包到Host
* 输入参数: pPackSent
* 输出参数: void
* 返 回 值: void
* 创建日期: 2018年01月01日
* 注    意: 
*********************************************************************************************************/
static  void  SendPackToHost(StructPackType* pPackSent)
{
  u8  packLen;                    //包长

  packLen = PackData(pPackSent);  //调用打包函数来打包数据
  
  if(0 < packLen)                //打包成功
  {
    WriteUART1((u8*)pPackSent, 10); //写入数据到串口
  }
}


/*********************************************************************************************************
* 函数名称: SendAckPack
* 函数功能: 发送响应包
* 输入参数: moduleId，secondId，ackMsg
* 输出参数: void
* 返 回 值: void
* 创建日期: 2018年01月01日
* 注    意: 
*********************************************************************************************************/
void SendAckPack(u8 moduleId, u8 secondId, u8 ackMsg)
{
  StructPackType pt;           //包结构体

  pt.packModuleId = MODULE_SYS;   //系统信息
  pt.packSecondId = DAT_CMD_ACK;  //命令应答
  pt.arrData[0] = moduleId;       
  pt.arrData[1] = secondId;
  pt.arrData[2] = ackMsg;
  pt.arrData[3] = 0;
  pt.arrData[4] = 0;
  pt.arrData[5] = 0;

  SendPackToHost(&pt);//调用打包函数来打包数据，并数据发送到Host
}

/*********************************************************************************************************
* 函数名称: SendWaveToHost
* 函数功能: 发送wave到Host，一次性发送5个点
* 输入参数: pWaveData-wave1波形数据，0-255
* 输出参数: void
* 返 回 值: void
* 创建日期: 2018年01月01日
* 注    意: 
*********************************************************************************************************/
void  SendWaveToHost(u8* pWaveData)
{
  StructPackType  pt;         //包结构体
  
  pt.packModuleId = MODULE_WAVE;    //波形信息
  pt.packSecondId = DAT_WAVE_WDATA; //波形数据
  pt.arrData[0] = pWaveData[0];     //波形的点数据
  pt.arrData[1] = pWaveData[1];
  pt.arrData[2] = pWaveData[2];
  pt.arrData[3] = pWaveData[3];
  pt.arrData[4] = pWaveData[4];
  pt.arrData[5] = 0;        //保留

  SendPackToHost(&pt);      //调用打包函数来打包数据，并数据发送到Host
}

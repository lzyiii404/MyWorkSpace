/*********************************************************************************************************
*                                              内部函数声明
*********************************************************************************************************/
static  void ConfigNVIC(void);     //配置NVIC中断优先级管理

/*********************************************************************************************************
*                                              内部函数实现
*********************************************************************************************************/

/*********************************************************************************************************
* 函数名称: ConfigNVIC
* 函数功能: 配置中断NVIC
* 输入参数: void
* 输出参数: void
* 返 回 值: void
* 创建日期: 2018年01月01日
* 注    意: 
*********************************************************************************************************/
static  void ConfigNVIC(void)
{
  NVIC_PriorityGroupConfig(NVIC_PriorityGroup_2); //设置NVIC中断分组2:2位抢占优先级，2位响应优先级
}

/*********************************************************************************************************
*                                              API函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称: InitNVIC
* 函数功能: 初始化NVIC 
* 输入参数: void
* 输出参数: void
* 返 回 值: void
* 创建日期: 2018年01月01日
* 注    意: 
*********************************************************************************************************/
void InitNVIC(void)    
{
  ConfigNVIC();      //配置NVIC中断优先级管理
}

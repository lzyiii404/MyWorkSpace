/*********************************************************************************************************
* 函数名称: ProcKeyDownKey2
* 函数功能: 处理按键按下的事件，即按键按下的响应函数 
* 输入参数: void
* 输出参数: void
* 返 回 值: void
* 创建日期: 2018年01月01日
* 注    意: 
*********************************************************************************************************/
void  ProcKeyDownKey2(void)
{
  printf("KEY2 PUSH DOWN\r\n");   //打印按键状态
  for(tmp = 0; tmp != 5; tmp++){
    LED_1(array[tmp % 4] / 10);
    LED_2(array[tmp % 4] % 10);
    delay();
  }
  tmp = 0;
}


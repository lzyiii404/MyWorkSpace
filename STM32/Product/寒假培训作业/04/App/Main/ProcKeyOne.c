/*********************************************************************************************************
*                                              宏定义
*********************************************************************************************************/
#define   LED_1(a)     if(a)\
                        GPIO_SetBits(GPIOC, GPIO_Pin_4);\
                      else\
                        GPIO_ResetBits(GPIOC, GPIO_Pin_4);

#define   LED_2(a)     if(a)\
                        GPIO_SetBits(GPIOC, GPIO_Pin_5);\
                      else\
                        GPIO_ResetBits(GPIOC, GPIO_Pin_5);  
/*********************************************************************************************************
*                                              枚举结构体定义
*********************************************************************************************************/

/*********************************************************************************************************
*                                              内部变量
*********************************************************************************************************/
u32 array[] = {00, 01, 10, 11};
int tmp = 0;
/*********************************************************************************************************
*                                              内部函数声明
*********************************************************************************************************/
void delay(){
  u32 i = 0x5fffff;
  for(; i != 0; i--);
}

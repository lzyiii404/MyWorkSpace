/*********************************************************************************************************
* 函数名称: ProcHostCmd
* 函数功能: 处理HOST发送来的命令 
* 输入参数: recData
* 输出参数: void
* 返 回 值: void
* 创建日期: 2018年01月01日
* 注    意: 
*********************************************************************************************************/
void ProcHostCmd(u8 recData)
{ 
  u8 ack;                 //存储返回数据
  
  while(UnPackData(recData))  //解包成功
  {
    pack = GetUnPackRslt(); //获取打包结果    
    
    switch(pack.packModuleId)  //选择包ID
    {
      case MODULE_WAVE:     //波形信息
        ack = OnGenWave(0);  //波形生成
        SendAckPack(MODULE_WAVE, CMD_GEN_WAVE, ack);  //发送响应包
        break;
      default:     
        break;
    }
  }   
}

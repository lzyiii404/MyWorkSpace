/*********************************************************************************************************
*                                              内部函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称: OnGenWave
* 函数功能: 生成Wave
* 输入参数: pMsg
* 输出参数: void
* 返 回 值: 响应结果
* 创建日期: 2018年01月01日
* 注    意: 
*********************************************************************************************************/
u8 OnGenWave(u8 flag) 
{  
  StructDACWave wave;                         //DAC数据波形数组
  
  flag ? (pack.arrData[0] == 0x02 ? pack.arrData[0] = 0x00 : pack.arrData[0]++) : flag;

  OLEDClear();
  if(pack.arrData[0] == 0x00)                      
  {
    wave.waveBufAddr  = (u32)GetSineWave100PointAddr();  //获取正弦波数组的地址 
    OLEDShowString(24, 24, "SIN");
  }
  else if(pack.arrData[0] == 0x01)
  {
    wave.waveBufAddr  = (u32)GetTriWave100PointAddr();   //获取三角波数组的地址 
    OLEDShowString(24, 24, "TRI");
  }
  else if(pack.arrData[0] == 0x02)
  {
    wave.waveBufAddr  = (u32)GetRectWave100PointAddr();  //获取方波数组的地址 
    OLEDShowString(24, 24, "RECT");
  }

  OLEDRefreshGRAM();
  
  wave.waveBufSize  = 200;       //Buf大小为100

  SetDACWave(wave);     //设置波形
  
  return(CMD_ACK_OK);            //返回命令成功
}

